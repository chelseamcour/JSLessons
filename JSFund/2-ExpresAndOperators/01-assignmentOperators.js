
//Assignment Operator: =
let x = 5;

//Addition Operator: +
x = x + 1;
x += 1;

//Subtraction Operator: -
x = x - 1;
x -= 1;

//Multiplication Operator: *
x = x * 1;
x *= 1;


//Division Operator: /
x = x / 1;
x /= 1;

//Remainder Operator: % (modulus operator)
x = x % 1;
x %= 1;

//Exponential Operator: ** (to the __ power)
x = x ** 1;
x **= 1;
