//falsey values: undefined, null, NaN (not a number), 0, "" (empty string), false

//Equal: ==
"3" == 3;

//Strict Equal: ===
"3" === 3;

//Not Equal: !=
"3" != 4;

//Strict Not Equal: !==
"3" !== 3;

//Greater Than: >
3 > 2;

//<Less Than: <
3 < 2;

//Greater Than or Equal To: >=
3 >= 1; //not =>, that's arrow function

//Less Than or Equal To: <=
3 <= 2;

//And: && 
2 > 1 && 2 < 5;

//Or: ||
2 > 1 || 2 < 5;